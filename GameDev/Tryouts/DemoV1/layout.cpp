int
get_tile_type( int x, int y, int scr )
{
    int div_x = x / TILE_WIDTH;
    //int rem_x = x % TILE_WIDTH;

    int div_y = y / TILE_WIDTH;
    //int rem_y = y % TILE_WIDTH;

    //if( rem_x > 0 ){
        //div_x += 1;
    //}

    //if( rem_y > 0 ){
        //div_y += 1;
    //}

    div_x = div_x % ( SCREEN_WIDTH / TILE_WIDTH );

    //printf("x = %d\n", div_x);
    //printf("y = %d\n", div_y);

    //printf( "TILE = %d\n", tile_map[ (28 * (SCREEN_WIDTH / 16)) + 40 ]);
    return tile_map[scr][ (div_y * (SCREEN_WIDTH / TILE_WIDTH)) + div_x ];
    //return 0; 
}

void
draw_layout( SDL_Surface **src, SDL_Surface **dest, SDL_Rect *src_rect, int scr )
{
    for(int i=0;i<SCREEN_HEIGHT / TILE_WIDTH;i++){
        for(int j=0;j<SCREEN_WIDTH / TILE_WIDTH;j++){
            //printf("Tile i = %d, j = %d, index = %d | -> %d\n", i, j, (i * (SCREEN_WIDTH / 16)) + j, tile_map[ (i * (SCREEN_WIDTH / 16)) + j ]);
            //if( tile_map[i * (SCREEN_HEIGHT / 16) + j]  == 1){ 
            if( tile_map[scr][(i * (SCREEN_WIDTH / TILE_WIDTH)) + j]  == 1 ){ 
                //apply_surface( j * 16, i * 16, bricks, *dest );
                
                apply_surface( j * TILE_WIDTH, i * TILE_WIDTH, *src, *dest, (src_rect + 0) );
                //printf("Tile 01\n");
            }else if( tile_map[scr][(i * (SCREEN_WIDTH / TILE_WIDTH)) + j]  == 2 ){
                apply_surface( j * TILE_WIDTH, i * TILE_WIDTH, *src, *dest, (src_rect + 1) );
            }else if( tile_map[scr][(i * (SCREEN_WIDTH / TILE_WIDTH)) + j]  == 3 ){
                apply_surface( j * TILE_WIDTH, i * TILE_WIDTH, *src, *dest, (src_rect + 2) );
            }
        }
    }

    //printf("Tile Last -> %d\n", tile_map[ 29 * 40 ]);
}

