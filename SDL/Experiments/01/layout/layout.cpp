void layout_draw( SDL_Surface *screen )
{
    for(int i=0;i<SCREEN_HEIGHT;i++){
        for(int j=0;j<SCREEN_WIDTH;j++){
            if( layout[i][j] == 0x00 ){
            
            SDL_Rect tmp;

            tmp.x = (j * 32);
            tmp.y = (i * 32);
            tmp.w = 32;
            tmp.h = 32;

            // Blitting object
            SDL_BlitSurface(smap, NULL, screen, &tmp);
            } 
        }
    }
}

